package com.example.e_comerce.request;

import jakarta.validation.constraints.Email;    // Import this
import jakarta.validation.constraints.NotBlank; // Import this
import jakarta.validation.constraints.Size;     // Import this

public class LoginRequest {
    @NotBlank(message = "Email is required")
    @Email(message = "Invalid email format")
    private String email;

    @NotBlank(message = "Password is required")
    @Size(min = 6, message = "Password must be at least 6 characters long") // Assuming a minimum password length
    private String password;

    public LoginRequest() {
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }
}
